#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	int		size_src;
	char	*dest;

	size_src = 0;
	i = 0;
	while (src[size_src])
		size_src++;
	dest = (char*)malloc(size_src + 1 * sizeof(char));
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
